Waste management in India falls under the preview of the Union Ministry of Environment, Forests and Climate Change (MoEF&CC). 

In 2016 this ministry released the Solid Waste Management (SWM) Rules,2016, these rules replaced the Municipal Solid Wastes Rules.
Urban India (about 377 million people) generates 62 million tonnes of municipal solid waste each year, of this about 43 million tonnes (70%) is collected
and 11.9 million tonnes (20%) is treated.

About 31 million tonnes (50%) is dumped in landfill sites.With changing consumption patterns and rapid economic growth 
it is estimated that urban municipal solid waste generation will increase to 165 million tonnes in 2030.

<b> Waste Collection in India: Primarily by the city municipality </b> 

	• No gradation of waste product eg bio-degradable, glasses, poly bags, paper shreds etc. 
	• Dumps these wastes to the city outskirts 

<b>Local raddiwala / kabadiwala (Rag pickers) </b>
	
	• Collecting small iron pieces by magnets 
	• Collecting glass bottles 
	• Collecting paper for recycling

It is suffice to say that we require a more stringent integrated and strategic waste prevention framework to effectively address wastage related issues. 

There is an urgent need to build upon existing systems instead of attempting to replace them blindly with models from developed countries.

To prevent any epidemic and to make each city a healthy city-economically and environmentally, there is an urgent need for a well-defined strategic waste management plan and a strong implementation of the same in India. 

To achieve financial sustainability, socio-economic and environmental goals in the field of waste management, there is a need to systematically analyze the strengths and weaknesses of the community
as well as the municipal corporation, based on which an effective waste management system can be evolved with the participation of various stakeholders in India. 

The public apathy can be altered by awareness building campaigns and educational measures. Sensitization of the community is also essential to achieve the above objectives and we need to act and act fast as every city in India 
is already a hotbed of many contagious diseases, most of which are caused by ineffective waste management.
