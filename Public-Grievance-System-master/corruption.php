Corruption is an issue which adversely affects India's economy of central, state and local government agencies. 
Not only has it held the economy back from reaching new heights,but rampant corruption has stunted the country's development.
A study conducted by Transparency International in 2005 recorded that more than 62% of Indians had at some point or another paid a bribe to a public official to get a job done.
In 2008, another report showed that about 50% of Indians had first hand experience of paying bribes or using contacts to get services performed by public offices, 
however, in 2018 their Corruption Perception Index ranked the country 78th place out of 180, reflecting steady decline in perception of corruption among people.

The causes of corruption in India include excessive regulations, complicated tax and licensing systems, numerous government departments with opaque bureaucracy and discretionary powers,
monopoly of government controlled institutions on certain goods and services delivery, and the lack of transparent laws and processes.


<b>Anti-corruption efforts</b>

1)Right to Information Act
2)Right to public legislation

<b>Anti-corruption laws in India </b>

Public servants in India can be imprisoned for several years and penalised for corruption under the:
    • Indian Penal Code, 1860
    • Prosecution section of Income Tax Act, 1961
    • The Prevention of Corruption Act, 1988
    • The Benami Transactions (Prohibition) Act, 1988 to prohibit benami transactions.
    • Prevention of Money Laundering Act, 2002

<b>Anti-corruption police and courts</b>

The Directorate General of Income Tax Investigation, Central Vigilance Commission and Central Bureau of Investigation all deal with anti-corruption initiatives. 
Certain states such as Andhra Pradesh and Karnataka (Lokayukta) also have their own anti-corruption agencies and courts.

<b>Impact of corruption </b>
	• Loss of credibility
	• Economic loss
