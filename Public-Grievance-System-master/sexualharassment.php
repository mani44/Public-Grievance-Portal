The Sexual Harassment of Women at Workplace (Prevention, Prohibition and Redressal) Act, 2013 is a legislative act in India that seeks to protect women from sexual 
harassment at their place of work. 

It was passed by the Lok Sabha (the lower house of the Indian Parliament) on 3 September 2012. It was passed by the Rajya Sabha (the upper house of the Indian 
Parliament) on 26 February 2013. 

The Bill got the assent of the President on 23 April 2013.The Act came into force from 9 December 2013.This statute superseded the Vishakha Guidelines for prevention
of sexual harassment introduced by the Supreme Court of India. It was reported by the International Labour Organization that very few Indian employers were compliant 
to this statute.

The Act uses a definition of sexual harassment which was laid down by the Supreme Court of India in Vishaka v. State of Rajasthan (1997). 
Article 19 (1) g of the Indian Constitution affirms the right of all citizens to be employed in any profession of their choosing or to practice their 
own trade or business. 

Vishaka v. State of Rajasthan established that actions resulting in a violation of one's rights to ‘Gender Equality’ and ‘Life and Liberty’ are in fact a violation of 
the victim’s fundamental right under Article 19 (1) g. 

The case ruling establishes that sexual harassment violates a woman's rights in the workplace and is thus not just a matter of personal injury.This case ruling had 
issued Vishaka guidelines under Article 32 of the Constitution of India. 
The Supreme Court had made it mandatory that these had to be followed by all originations until a legislative framework on the subject has been drawn-up and enacted.

<b>Major features</b>

    • The Act defines sexual harassment at the work place and creates a mechanism for redressal of complaints. 
      It also provides safeguards against false or malicious charges.

    • The Act also covers concepts of 'quid pro quo harassment' and 'hostile work environment' as forms of sexual harassment 
      if it occurs in connection with an act or behaviour of sexual harassment.

    • The definition of "aggrieved woman", who will get protection under the Act is extremely wide to cover all women, 
      irrespective of her age or employment status, whether in the organised or unorganised sectors,public or private and covers clients,
      customers and domestic workers as well. 
