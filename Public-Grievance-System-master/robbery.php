<pre>
Robbery is the crime of taking or attempting to take anything of value by force, threat of force, or by putting the victim in fear. 
According to common law, robbery is defined as taking the property of another, with the intent to permanently deprive the person of that property, by means of force or fear; 
that is, it is a larceny or theft accomplished by an assault

<b>Section 378 in The Indian Penal Code </b>

378 Theft. -Whoever, intending to take dishonestly any movable property out of the possession of any person without that per­son’s consent, 
moves that property in order to such taking, is said to commit theft. 

Explanation 1  A thing so long as it is attached to the earth, not being movable property, is not the subject of theft; 
	       but it becomes capable of being the subject of theft as soon as it is severed from the earth. 
Explanation 2. A moving effected by the same act which affects the severance may be a theft. 
Explanation 3. A person is said to cause a thing to move by removing an obstacle which prevented it from moving or by 
	       sepa­rating it from any other thing, as well as by actually moving it. 
Explanation 4 A person, who by any means causes an animal to move, is said to move that animal, and to move everything which, 
	      in consequence of the motion so caused, is moved by that animal. 
Explanation 5. The consent mentioned in the definition may be express or implied, and may be given either by the person in 
	       possession, or by any person having for that purpose authority either express or implied. 

<b>Illustrations </b>

1) A cuts down a tree on Z’s ground, with the intention of dis­honestly taking the tree out of Z’s possession without Z’s con­sent. 
   Here, as soon as A has severed the tree in order to such taking, he has committed theft.
2)A puts a bait for dogs in his pocket, and thus induces Z’s dog to follow it. Here, if A’s intention be dishonestly to take the dog out of Z’s possession without Z’s consent. 
  A has commit­ted theft as soon as Z’s dog has begun to follow A.
3)A meets a bullock carrying a box of treasure. He drives the bullock in a certain direction, in order that he may dishonestly take the treasure. 
  As soon as the bullock begins to move, A has committed theft of the treasure.
4)A, being Z’s servant, and entrusted by Z with the care of Z’s plate, dishonestly runs away with the plate, without Z’s consent. 
  A has committed theft.
5)Z, going on a journey, entrusts his plate to A, the keeper of the warehouse, till Z shall return. A carries the plate to a goldsmith and sells it. 
  Here the plate was not in Z’s possession. It could not therefore be taken out of Z’s possession, and A has not committed theft, 
  though he may have committed criminal breach of trust.
