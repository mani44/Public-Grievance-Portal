Everyone is waiting for rainy season and now finally after a long wait the season of rainy is finally going to be arrived. But what is the first thought which comes into general public minds when we have our anticipation about the rains??? 

Hmmm... hold on... let me share you my intuition about it. 

It’s about leakage, flat’s walls getting wet during the monsoon and the list goes on. There is often a lot of dispute and complaints about it between the society and its members and the society and its committee do not respond 
to any of the complains of its members that who shall be really held responsible for it. And now here is the answer for it.

<b>The answer of your each and every questions and dilemma is cited under section 160 of model bye-laws  which says that the following Repairs and Maintenance of the Society's property shall be carried out by the society at its cost:</b>

	(i) All Internal Roads
	(ii) Compound walls
	(iii) External water pipes
	(iv)Water pumps
	(v) Water storage tanks
	(vi) Drainage lines
	(vii) Septic tanks

All these are exhaustive in nature and the repairs not covered by the underneath Bye-law, shall be carried out by the members at their cost

Accordingly, if there is leakage on the external walls of your flat leading to leakage within, the onus of repairs falls squarely on the Society at its cost. 
Send the managing committee a legal notice for the same through a lawyer and follow up with the secretary of the society.
There is also a prominent judgment referring the above matter given by Bombay high court in the case of Humble Home Co-Op Housing Society Limited V/S Sham Balani 
And Another Decided On Wednesday, June 28, 2006 and its being ordered in the above matter that Terrace repairs is the responsibility of the society .
The first issue has been answered by holding that the terrace which forms the roof of the disputant's lat is the property of the society and therefore, in reply to issue No. 2,
the society has been directed to pay an amount of  8.458/-

<b>Other solutions for solving internal flat leakage problem:</b>
    
    1)File police complaint.
    2)Mandatory injunction against him/her for carrying out the repairs to the seepage or file a civil suit for injunction and obtain stay order restraining that resident from using the area which is the cause for the seepage
    3)For quick response file a petition u/s 133 crpc in the court of local 2nd Class Magistrate.
    4)Criminal cases can also be filed against him/her with charges of criminal intimidation and verbal abuses/insult.
