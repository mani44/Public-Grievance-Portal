The Ministry of Road Transport and Highways has taken a some steps to prevent road accidents and road accident fatalities . These include
    1. The Government has approved a National Road Safety Policy.This Policy outlines various policy measures such as promoting awareness, establishing 
       road safety information data base, encouraging safer road infrastructure including application of intelligent transport, enforcement of safety laws etc.
    2. The Government has constituted the National Road Safety Council as the apex body to take policy decisions in matters of road safety.

Road accident is most unwanted thing to happen to a road user, though they happen quite often. The most unfortunate thing is that we don't learn from our mistakes on road. 
Most of the road users are quite well aware of the general rules and safety measures while using roads but it is only the laxity on part of road users, which cause accidents and crashes. 
Main cause of accidents and crashes are due to human errors. We are elaborating some of the common behaviour of humans which results in accident.
1.Over Speeding
2.Drunken Driving
3.Distractions to Driver
4.Red Light Jumping
5.Avoiding Safety Gears like Seat belts and Helmets
6.Non-adherence to lane driving and overtaking in a wrong manner

<b>How different factors of Roads contribute in Accidents:</b>
Drivers: Over-speeding, rash driving, violation of rules, failure to understand signs, fatigue, alcohol.
Pedestrian: Carelessness, illiteracy, crossing at wrong places moving on carriageway, Jaywalkers.
Passengers: Projecting their body outside vehicle, by talking to drivers, alighting and boarding vehicle from wrong side travelling on footboards, catching a running bus etc.
Vehicles: Failure of brakes or steering, tyre burst, insufficient headlights, overloading, projecting loads.
Road Conditions: Potholes, damaged road, eroded road merging of rural roads with highways, diversions, illegal speed breakers.
Weather conditions:Fog, snow, heavy rainfall, wind storms, hail storms.

<b>Preventive measures for accidents:</b>
    1. Education and awareness about road safety
    2. Strict Enforcement of Law
    3. Engineering:
	(a) Vehicle design 
	(b) Road infrastructure

<b>Direct Consequences of Accidents:</b>
1 Fatality (Death) 2. Injury 3. Property Damage
