Domestic violence in India includes any form of violence suffered by a person from a biological relative, but typically is the violence suffered by a woman by male 
members of her family or relatives.
According to a National Family and Health Survey in 2005, total lifetime prevalence of domestic violence was 33.5% and 8.5% for sexual violence among women aged 15–49.

There are several domestic violence laws in India. The earliest law was the Dowry Prohibition Act 1961 which made the act of giving and receiving dowry a crime. In an 
effort to bolster the 1961 law, two new sections, 

Section 498A and Section 304B were introduced into the Indian Penal Code in 1983 and 1986. The most recent legislation is the Protection of Women from Domestic 
Violence Act (PWDVA) 2005. The PWDVA, a civil law, includes physical, emotional, sexual, verbal, and economic abuse as domestic violence.

<b>Consequences of Domestic Violence</b>

There are varied consequences of domestic violence depending on the victim, the age group, the intensity of the violence and frequency of the torment they are 
subjected to. 

Living under a constant fear, threat and humiliation are some of the feelings developed in the minds of the victims as a consequence of an atrocious violence. 

The consequences of the domestic violence in detail can be broadly categorised under

1)The Effect on the victim himself/herself and the family
2)Effect on the society 
3)The Effect on nation’s growth and productivity. 

<b>Effect of Domestic Violence on the society</b>

All the different forms of violence discussed in this essay adversely affect the society. Violence against women may keep them locked in homes succumbing to the 
torture they face. 
If they come out in open and reveal the wrong done to them for help and rescue, it influences the society both positively and negatively. At one hand where it acts as
an inspiration and ray of hope for other suffering women, on the other hand it also spoils the atmosphere of the society. When something of this kind happens in the
society, few families may witness the evil of domestic violence knocking their door steps. 

Some families try to imitate what others indulge in irrespective of it being good or bad for the family.
